void solve(int casenum)
{

    int n;
    cin>>n;
    int ans = 0, one = 0;
    for(int i=1, x; i<=n; i++)
    {
        cin>>x;
        ans ^= x;
        if(x == 1) one++;
    }

    cout<<"Case "<<casenum<<": ";

    if(one == n)
    {
        if(one % 2) cout<<"Bob"<<endl;
        else cout<<"Alice"<<endl;
    }
    else
    {
        if(ans == 0) cout<<"Bob"<<endl;
        else cout<<"Alice"<<endl;
    }
}

