void solve(int casenum)
{
    int days;
    cin>>days;

    int person = 0, rem_days = days;
    double p = 0, prev_p = 1;
    while(1)
    {
        double cur_p = double(rem_days) / double(days);
        cur_p *= prev_p;
        p = 1 - cur_p;
        if(p >= 0.5) break;
        person++;
        prev_p = cur_p;
        rem_days--;
    }
    cout<<"Case "<<casenum<<": "<<person<<endl;
}

