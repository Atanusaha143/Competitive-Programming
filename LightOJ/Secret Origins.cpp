map<LL,int>mp,revmp;

void precal()
{
    for(LL i=0; i<=35; i++)
    {
        LL cur=(1LL<<i);
        mp[cur]=i;
        revmp[i]=cur;
    }
}

int process(int n)
{
    // convert to bin
    string x="";
    for(int i=31; i>=0; i--)
    {
        int k=1<<i;
        bool status=(bool)(n&k);
        if(status) x.PB('1');
        else x.PB('0');
    }

    // get next permutation of it
    next_permutation(all(x));

    // convert to dec
    LL ans=0;
    for(LL i=sz(x)-1,j=0; i>=0; i--,j++)
    {
        if(x[i]=='1') ans+=(1LL<<j);
    }
    return ans;
}

void solve(int casenum)
{
    LL n;
    cin>>n;

    cout<<"Case "<<casenum<<": ";
    if(mp.find(n)!=mp.end())
    {
        LL id=mp[n];
        LL ans=revmp[id+1];
        cout<<ans<<NL;
        return;
    }
    else
    {
        int res=process(n);
        cout<<res<<NL;
    }
    
}

