const int N = 1e6 + 10;
const int MOD = 1000000007;

int n, q;
int arr[N];

void solve(int casenum)
{
    cin>>n>>q;
    for(int i=1; i<=n; i++)
    {
        cin>>arr[i];
    }

    cout<<"Case "<<casenum<<":"<<endl; 
    while(q--)
    {
        int l, r;
        cin>>l>>r;
        l++, r++;
        int diff = r - l;
        if(diff > 1000)
        {
            cout<<"0"<<endl;
        }
        else
        {
            int len = (r - l) + 1, id = 0;
            int brr[len+1] =  {0};
            for(int i=l; i<=r; i++)
            {
                brr[++id] = arr[i];
            }
            sort(brr+1,brr+id+1);
            int ans = 2000;
            for(int i=1; i<id; i++)
            {
                int dif = brr[i+1] - brr[i];
                ans = min( ans,dif );
            }
            cout<<ans<<endl;
        }
    }
}

